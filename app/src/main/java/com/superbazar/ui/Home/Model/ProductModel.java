package com.superbazar.ui.Home.Model;

public class ProductModel {
    String id,name,image;

    public ProductModel(String id, String name, String image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }
}
